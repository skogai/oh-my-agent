import {
  cpSync,
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  writeFileSync,
} from "node:fs";
import { join } from "node:path";
import {
  parseFrontmatter,
  serializeFrontmatter,
} from "../utils/frontmatter.js";

/** SSOT for all rules. */
export const RULES_DIR = ".agents/rules";

const CURSOR_RULES_DIR = ".cursor/rules";
const CLAUDE_RULES_DIR = ".claude/rules";

interface ParsedRule {
  name: string;
  description: string;
  globs: string;
  alwaysApply: boolean;
  body: string;
}

/** Read and parse all .md rule files from the SSOT directory. */
export function readRules(baseDir: string): ParsedRule[] {
  const rulesDir = join(baseDir, RULES_DIR);
  if (!existsSync(rulesDir)) return [];

  return readdirSync(rulesDir)
    .filter((f) => f.endsWith(".md"))
    .map((file) => {
      const content = readFileSync(join(rulesDir, file), "utf-8");
      const { frontmatter, body } = parseFrontmatter(content);
      return {
        name: file.replace(".md", ""),
        description:
          (frontmatter.description as string) || file.replace(".md", ""),
        globs: (frontmatter.globs as string) || "",
        alwaysApply: (frontmatter.alwaysApply as boolean) || false,
        body: body.trim(),
      };
    });
}

// =============================================================================
// Cursor (.cursor/rules/*.mdc)
// =============================================================================

/**
 * Generate .cursor/rules/*.mdc from SSOT.
 * Cursor uses: description, globs, alwaysApply frontmatter.
 */
export function applyCursorRules(targetDir: string): string[] {
  const rules = readRules(targetDir);
  if (rules.length === 0) return [];

  const cursorDir = join(targetDir, CURSOR_RULES_DIR);
  mkdirSync(cursorDir, { recursive: true });

  const exported: string[] = [];

  for (const rule of rules) {
    const header = `<!-- Generated by oh-my-agent. Source: ${RULES_DIR}/${rule.name}.md -->`;
    const content = [
      "---",
      `description: ${JSON.stringify(rule.description)}`,
      `globs: ${rule.globs ? JSON.stringify(rule.globs) : ""}`,
      `alwaysApply: ${rule.alwaysApply}`,
      "---",
      "",
      header,
      "",
      rule.body,
      "",
    ].join("\n");

    writeFileSync(join(cursorDir, `${rule.name}.mdc`), content);
    exported.push(rule.name);
  }

  return exported;
}

// =============================================================================
// Claude Code (.claude/rules/*.md)
// =============================================================================

/**
 * Generate .claude/rules/*.md from SSOT.
 * Claude Code uses: paths (mapped from globs) frontmatter.
 */
export function generateClaudeRules(targetDir: string): string[] {
  const rules = readRules(targetDir);
  if (rules.length === 0) return [];

  const claudeDir = join(targetDir, CLAUDE_RULES_DIR);
  mkdirSync(claudeDir, { recursive: true });

  const exported: string[] = [];

  for (const rule of rules) {
    const fm: Record<string, unknown> = {
      description: rule.description,
    };

    if (!rule.alwaysApply && rule.globs) {
      fm.paths = rule.globs;
    }

    const content = serializeFrontmatter(fm, `\n${rule.body}\n`);
    writeFileSync(join(claudeDir, `${rule.name}.md`), content);
    exported.push(rule.name);
  }

  return exported;
}

// =============================================================================
// Raw copy (SSOT → SSOT in target)
// =============================================================================

export function installRulesRaw(sourceDir: string, targetDir: string): void {
  const src = join(sourceDir, RULES_DIR);
  if (!existsSync(src)) return;

  const dest = join(targetDir, RULES_DIR);
  mkdirSync(dest, { recursive: true });
  cpSync(src, dest, { recursive: true, force: true });
}

