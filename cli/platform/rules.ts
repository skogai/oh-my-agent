/** Prefix used for matching any OMA:START variant. */
const OMA_START_PREFIX = "<!-- OMA:START";

/**
 * Merge OMA block into a target markdown file using markers.
 * Preserves any user content outside the OMA block.
 */
function mergeOmaBlock(filePath: string, block: string): void {
  if (existsSync(filePath)) {
    const existing = readFileSync(filePath, "utf-8");
    const startIdx = existing.indexOf(OMA_START_PREFIX);
    const endIdx = existing.indexOf(OMA_END);

    if (startIdx !== -1 && endIdx !== -1) {
      const before = existing.slice(0, startIdx);
      const after = existing.slice(endIdx + OMA_END.length);
      writeFileSync(filePath, `${before}${block}${after}`);
    } else {
      writeFileSync(filePath, `${existing.trimEnd()}\n\n${block}\n`);
    }
  } else {
    writeFileSync(filePath, `${block}\n`);
  }
}

/**
 * Generate and merge the OMA block (usage guide + rules index)
 * into the vendor's local markdown file.
 */
export function mergeRulesIndexForVendor(
  targetDir: string,
  vendor: string,
): boolean {
  const fileName = VENDOR_FILES[vendor];
  if (!fileName) return false;

  const rules = readRules(targetDir);
  const block = buildVendorBlock(vendor, rules);
  mergeOmaBlock(join(targetDir, fileName), block);
  return true;
}
